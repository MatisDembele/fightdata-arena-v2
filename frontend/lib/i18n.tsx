const dict: Record<string, Record<DictKey, string>> = { en, fr, es, ja }

export type { DictKey }
export type Lang = 'en' | 'fr' | 'es' | 'ja'

interface LanguageContextValue {
  lang: Lang
  setLang: (l: Lang) => void
  t: (key: DictKey, vars?: Record<string, string | number>) => string
}

const LanguageContext = createContext<LanguageContextValue | null>(null)

function getInitialLang(): Lang {
  if (typeof window === 'undefined') return 'en'
  const stored = localStorage.getItem('fda_lang')
  if (stored === 'en' || stored === 'fr' || stored === 'es' || stored === 'ja') return stored as Lang
  return 'en'
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(getInitialLang)

  useEffect(() => { setLangState(getInitialLang()) }, [])

  const setLang = useCallback((l: Lang) => {
    setLangState(l)
    localStorage.setItem('fda_lang', l)
  }, [])

  const t = useCallback((key: DictKey, vars?: Record<string, string | number>): string => {
    let str: string = dict[lang]?.[key] ?? dict.en[key] ?? key
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
      }
    }
    return str
  }, [lang])

  return (
    <LanguageContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage(): LanguageContextValue {
  const ctx = useContext(LanguageContext)
  if (!ctx) throw new Error('useLanguage must be used inside LanguageProvider')
  return ctx
}
